package egov.service1;

public class RejectionVO {

	private int rejectionindex;
	private String rejection;
	private int oderindex;
	private String sort;
	
	
	public int getRejectionindex() {
		return rejectionindex;
	}
	public void setRejectionindex(int rejectionindex) {
		this.rejectionindex = rejectionindex;
	}
	public String getRejection() {
		return rejection;
	}
	public void setRejection(String rejection) {
		this.rejection = rejection;
	}
	public int getOderindex() {
		return oderindex;
	}
	public void setOderindex(int oderindex) {
		this.oderindex = oderindex;
	}
	public String getSort() {
		return sort;
	}
	public void setSort(String sort) {
		this.sort = sort;
	}
	
	
}
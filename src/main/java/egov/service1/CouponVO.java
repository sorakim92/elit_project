package egov.service1;

public class CouponVO {
	private int couponindex;
	private int storeunq;
	private int couponprice;
	private String createdat;
	private String valid;
	private String usedate;
	private String couponname;
	public int getCouponindex() {
		return couponindex;
	}
	public void setCouponindex(int couponindex) {
		this.couponindex = couponindex;
	}
	public int getStoreunq() {
		return storeunq;
	}
	public void setStoreunq(int storeunq) {
		this.storeunq = storeunq;
	}
	public int getCouponprice() {
		return couponprice;
	}
	public void setCouponprice(int couponprice) {
		this.couponprice = couponprice;
	}
	public String getCreatedat() {
		return createdat;
	}
	public void setCreatedat(String createdat) {
		this.createdat = createdat;
	}
	public String getValid() {
		return valid;
	}
	public void setValid(String valid) {
		this.valid = valid;
	}
	public String getUsedate() {
		return usedate;
	}
	public void setUsedate(String usedate) {
		this.usedate = usedate;
	}
	public String getCouponname() {
		return couponname;
	}
	public void setCouponname(String couponname) {
		this.couponname = couponname;
	}

}
